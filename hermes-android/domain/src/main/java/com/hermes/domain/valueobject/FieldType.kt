package com.hermes.domain.valueobject

/**
 * 扩展字段类型
 * FieldType - 账户扩展信息的数据类型
 */
enum class FieldType {
    /**
     * 字符串 - STRING
     */
    STRING,

    /**
     * 日期 - DATE
     */
    DATE,

    /**
     * 布尔 - BOOL
     */
    BOOL,

    /**
     * 枚举 - ENUM
     */
    ENUM,

    /**
     * 整数 - INTEGER
     */
    INTEGER
}