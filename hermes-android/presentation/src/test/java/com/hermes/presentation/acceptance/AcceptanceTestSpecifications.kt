/**
 * 预警级别计算规则：
 *
 * HIGH（紧急处理）触发条件：
 * - 影响金融类应用账号
 * - 影响账号数 > 5
 *
 * MEDIUM（建议处理）触发条件：
 * - 影响账号数 2-5（无金融应用）
 *
 * LOW（可稍后处理）触发条件：
 * - 影响账号数 = 1（非敏感账号）
 * - 影响账号数 = 0
 */
object WarningLevelRules {
    fun calculateLevel(affectedAccountCount: Int, hasFinancialAccount: Boolean): String {
        return when {
            hasFinancialAccount -> "HIGH"
            affectedAccountCount > 5 -> "HIGH"
            affectedAccountCount in 2..5 -> "MEDIUM"
            else -> "LOW"
        }
    }
}